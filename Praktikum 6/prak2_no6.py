def luasSegitiga (a,t):
    luas = (a*t) / 2
    return luas

alas=10
tinggi=20
print ('luas segitiga dgn alas  ', alas, '  dan tinggi ', tinggi, ' adalah ', luasSegitiga (alas , tinggi))

alas=15
tinggi=45
print ('luas segitiga kedua dengan alas  ', alas, '  dan tinggi ', tinggi, ' adalah ', luasSegitiga (alas , tinggi))
